import type { Node, Edge } from '@xyflow/react';
import type { FlowNodeData } from '../types';
import type { OopLang } from '../types';

function sanitize(label: string) {
  return label.replace(/[^a-zA-Z0-9_ ]/g, '').trim().replace(/\s+(.)/g, (_, c) => c.toUpperCase()) || 'step';
}
function snake(s: string) { return s.replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, ''); }

// ── Java (original) ───────────────────────────────────────────────────
function buildJava(nodeId: string, nodes: Node[], edges: Edge[], visited: Set<string>, indent: number): string[] {
  if (visited.has(nodeId)) return [`${' '.repeat(indent)}// (loop back)`];
  visited.add(nodeId);
  const node = nodes.find(n => n.id === nodeId);
  if (!node) return [];
  const d = node.data as unknown as FlowNodeData;
  const lines: string[] = [];
  const pad = ' '.repeat(indent);

  if (d.type === 'end') { lines.push(`${pad}return;`); return lines; }
  if (d.type === 'process') { lines.push(`${pad}${sanitize(d.label)}();`); }
  else if (d.type === 'io') {
    if (d.label.toLowerCase().includes('input') || d.label.toLowerCase().includes('read')) {
      lines.push(`${pad}Scanner scanner = new Scanner(System.in);`);
      lines.push(`${pad}String input = scanner.nextLine(); // ${d.label}`);
    } else { lines.push(`${pad}System.out.println(${JSON.stringify(d.label)});`); }
  } else if (d.type === 'decision') {
    const out = edges.filter(e => e.source === nodeId);
    const yes = out.find(e => (e.label as string)?.toLowerCase() === 'yes' || e.sourceHandle === 'yes');
    const no  = out.find(e => e !== yes);
    lines.push(`${pad}if (/* ${d.label} */) {`);
    if (yes) lines.push(...buildJava(yes.target, nodes, edges, new Set(visited), indent + 4));
    if (no)  { lines.push(`${pad}} else {`); lines.push(...buildJava(no.target, nodes, edges, new Set(visited), indent + 4)); }
    lines.push(`${pad}}`);
    return lines;
  }
  for (const e of edges.filter(e => e.source === nodeId))
    lines.push(...buildJava(e.target, nodes, edges, visited, indent));
  return lines;
}

function genJavaFlow(nodes: Node[], edges: Edge[]): string {
  const flow = nodes.filter(n => n.type === 'flowNode');
  const start = flow.find(n => (n.data as unknown as FlowNodeData).type === 'start');
  const procs = flow.filter(n => (n.data as unknown as FlowNodeData).type === 'process');
  const out: string[] = ['// Generated by UML→Code', '', 'public class FlowProcess {', '', '    public static void main(String[] args) {'];
  if (start) out.push(...buildJava(start.id, nodes, edges, new Set(), 8));
  else out.push('        // Connect a Start node to begin');
  out.push('    }', '');
  procs.forEach(n => {
    const d = n.data as unknown as FlowNodeData;
    const nm = sanitize(d.label);
    out.push(`    private static void ${nm}() {`, `        // TODO: implement ${d.label}`, '    }', '');
  });
  out.push('}');
  return out.join('\n');
}

// ── Python ────────────────────────────────────────────────────────────
function buildPy(nodeId: string, nodes: Node[], edges: Edge[], visited: Set<string>, indent: number): string[] {
  if (visited.has(nodeId)) return [`${' '.repeat(indent)}# (loop back)`];
  visited.add(nodeId);
  const node = nodes.find(n => n.id === nodeId);
  if (!node) return [];
  const d = node.data as unknown as FlowNodeData;
  const lines: string[] = [];
  const pad = ' '.repeat(indent);

  if (d.type === 'end') { lines.push(`${pad}return`); return lines; }
  if (d.type === 'process') lines.push(`${pad}${snake(sanitize(d.label))}()`);
  else if (d.type === 'io') {
    if (d.label.toLowerCase().includes('input')) lines.push(`${pad}data = input("${d.label}: ")`);
    else lines.push(`${pad}print(${JSON.stringify(d.label)})`);
  } else if (d.type === 'decision') {
    const out = edges.filter(e => e.source === nodeId);
    const yes = out.find(e => (e.label as string)?.toLowerCase() === 'yes' || e.sourceHandle === 'yes');
    const no  = out.find(e => e !== yes);
    lines.push(`${pad}if ${d.label.toLowerCase().replace(/[^a-z0-9_]/g, '_') || 'condition'}:`);
    if (yes) lines.push(...buildPy(yes.target, nodes, edges, new Set(visited), indent + 4));
    else lines.push(`${pad}    pass`);
    if (no)  { lines.push(`${pad}else:`); lines.push(...buildPy(no.target, nodes, edges, new Set(visited), indent + 4)); }
    return lines;
  }
  for (const e of edges.filter(e => e.source === nodeId))
    lines.push(...buildPy(e.target, nodes, edges, visited, indent));
  return lines;
}

function genPythonFlow(nodes: Node[], edges: Edge[]): string {
  const flow = nodes.filter(n => n.type === 'flowNode');
  const start = flow.find(n => (n.data as unknown as FlowNodeData).type === 'start');
  const procs = flow.filter(n => (n.data as unknown as FlowNodeData).type === 'process');
  const out: string[] = ['# Generated by UML→Code', '', 'def main():'];
  if (start) { const body = buildPy(start.id, nodes, edges, new Set(), 4); out.push(...body.length ? body : ['    pass']); }
  else out.push('    # Connect a Start node to begin', '    pass');
  out.push('', '');
  procs.forEach(n => {
    const nm = snake(sanitize((n.data as unknown as FlowNodeData).label));
    out.push(`def ${nm}():`, `    # TODO: implement`, '    pass', '');
  });
  out.push('', "if __name__ == '__main__':", '    main()');
  return out.join('\n');
}

