function genCppFlow(nodes: Node[], edges: Edge[]): string {
  const flow = nodes.filter(n => n.type === 'flowNode');
  const start = flow.find(n => (n.data as unknown as FlowNodeData).type === 'start');
  const procs = flow.filter(n => (n.data as unknown as FlowNodeData).type === 'process');
  const out: string[] = ['// Generated by UML→Code', '#include <iostream>', '#include <string>', '', ''];
  procs.forEach(n => {
    out.push(`void ${sanitize((n.data as unknown as FlowNodeData).label)}();`);
  });
  out.push('', 'int main() {');
  if (start) out.push(...buildCpp(start.id, nodes, edges, new Set(), 4));
  else out.push('    // Connect a Start node to begin');
  out.push('    return 0;', '}', '');
  procs.forEach(n => {
    const nm = sanitize((n.data as unknown as FlowNodeData).label);
    out.push(`void ${nm}() {`, `    // TODO: implement ${nm}`, '}', '');
  });
  return out.join('\n');
}

