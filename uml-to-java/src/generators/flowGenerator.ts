function genTsFlow(nodes: Node[], edges: Edge[], isJs = false): string {
  const flow = nodes.filter(n => n.type === 'flowNode');
  const start = flow.find(n => (n.data as unknown as FlowNodeData).type === 'start');
  const procs = flow.filter(n => (n.data as unknown as FlowNodeData).type === 'process');
  const ret = isJs ? '' : ': void';
  const fnKw = isJs ? 'function' : 'function';
  const out: string[] = [`// Generated by UML→Code`, '', `${fnKw} main()${ret} {`];
  if (start) out.push(...buildTs(start.id, nodes, edges, new Set(), 2));
  else out.push('  // Connect a Start node to begin');
  out.push('}', '');
  procs.forEach(n => {
    const nm = sanitize((n.data as unknown as FlowNodeData).label);
    out.push(`${fnKw} ${nm}()${ret} {`, `  // TODO: implement ${nm}`, '}', '');
  });
  out.push('main();');
  return out.join('\n');
}

