// ── Dispatcher ─────────────────────────────────────────────────────────
export function generateFlowCode(nodes: Node[], edges: Edge[], lang: OopLang = 'java'): string {
  const flowNodes = nodes.filter(n => n.type === 'flowNode');
  if (flowNodes.length === 0) return '// No flowchart nodes defined yet.';
  switch (lang) {
    case 'python':     return genPythonFlow(nodes, edges);
    case 'cpp':        return genCppFlow(nodes, edges);
    case 'csharp':     return genCsharpFlow(nodes, edges);
    case 'typescript': return genTsFlow(nodes, edges, false);
    case 'javascript': return genTsFlow(nodes, edges, true);
    case 'kotlin':     return genKotlinFlow(nodes, edges);
    case 'go':         return genGoFlow(nodes, edges);
    default:           return genJavaFlow(nodes, edges);
  }
}
