import type { Node, Edge } from '@xyflow/react';
import type { SequenceNodeData } from '../types';
import type { OopLang } from '../types';

/* ── helpers ────────────────────────────────────────────────────── */
function camel(s: string) {
  return s.replace(/[^a-zA-Z0-9]+(.)/g, (_, c: string) => c.toUpperCase())
          .replace(/[^a-zA-Z0-9]/g, '');
}
function snake(s: string) {
  return camel(s).replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, '');
}
function pascal(s: string) { const c = camel(s); return c.charAt(0).toUpperCase() + c.slice(1); }
function lower(s: string)  { const c = camel(s); return c.charAt(0).toLowerCase() + c.slice(1); }

/** Sort edges by sourceHandle "r-N" index (Y position on lifeline) */
function sortedMessages(allEdges: Edge[], allNodes: Node[]): Edge[] {
  const seqIds = new Set(allNodes.filter(n => n.type === 'sequenceNode').map(n => n.id));
  const msgs = allEdges.filter(e => seqIds.has(e.source));
  return [...msgs].sort((a, b) => {
    const parseH = (h: string | null | undefined, fallback: number) => {
      if (!h) return fallback * 100;
      const m = h.match(/[rl]-?(\d+)/);
      return m ? parseInt(m[1]) : fallback * 100;
    };
    return parseH(a.sourceHandle, msgs.indexOf(a)) - parseH(b.sourceHandle, msgs.indexOf(b));
  });
}

function msgLabel(e: Edge, i: number) {
  return ((e.label as string) || (e.data?.label as string) || `message${i + 1}`).trim();
}

function nameMap(nodes: Node[]) {
  return new Map(nodes.filter(n => n.type === 'sequenceNode').map(n => [
    n.id, (n.data as unknown as SequenceNodeData).label || 'Participant'
  ]));
}

/* ── Java ────────────────────────────────────────────────────────── */
function genJava(nodes: Node[], edges: Edge[]): string {
  const map = nameMap(nodes);
  const lifelines = [...map.entries()];
  if (!lifelines.length) return '// No lifelines yet.\n// Add Lifeline nodes to the canvas.';
  const msgs = sortedMessages(edges, nodes);
  const now = new Date().toISOString().slice(0, 10);

  const params = lifelines.map(([, name]) => `${pascal(name)} ${lower(name)}`).join(',\n            ');
  const lines = [
    `// Generated by UML→Code — Sequence Diagram`,
    `// Generated: ${now}`,
    `// Participants: ${lifelines.map(([, n]) => n).join(', ')}`,
    ``,
    `public class SequenceInteraction {`,
    ``,
    `    public static void execute(`,
    `            ${params}) {`,
    ``,
  ];

  msgs.forEach((e, i) => {
    const from = lower(map.get(e.source) ?? 'source');
    const to   = lower(map.get(e.target) ?? 'target');
    const msg  = lower(camel(msgLabel(e, i)));
    lines.push(`        // Step ${i + 1}: ${from} ──► ${to} : ${msg}`);
    lines.push(`        ${to}.${msg}(${from});`);
    lines.push(``);
  });

  if (!msgs.length) lines.push(`        // Connect lifelines with message arrows to generate interaction code`);
  lines.push(`    }`, `}`);
  return lines.join('\n');
}

