/* ── dispatcher ─────────────────────────────────────────────────── */
export function generateSequenceCode(nodes: Node[], edges: Edge[], lang: OopLang = 'java'): string {
  const seqNodes = nodes.filter(n => n.type === 'sequenceNode');
  if (!seqNodes.length) return '// No sequence diagram nodes yet.\n// Add Lifeline (Actor/Object) nodes to the canvas, then connect them with message arrows.';
  switch (lang) {
    case 'python':     return genPython(nodes, edges);
    case 'typescript': return genTs(nodes, edges, false);
    case 'javascript': return genTs(nodes, edges, true);
    case 'csharp':     return genCsharp(nodes, edges);
    case 'kotlin':     return genKotlin(nodes, edges);
    case 'go':         return genGo(nodes, edges);
    case 'ruby':       return genRuby(nodes, edges);
    default:           return genJava(nodes, edges);
  }
}
